package com.example.trt_figuresshop.Model;

public class NewProduct {
    int id;
    String tenfigures;
    String mota;
    String hinh;
    int giafigure;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenfigures() {
        return tenfigures;
    }

    public void setTenfigures(String tenfigures) {
        this.tenfigures = tenfigures;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }

    public String getHinh() {
        return hinh;
    }

    public void setHinh(String hinh) {
        this.hinh = hinh;
    }

    public int getGiafigure() {
        return giafigure;
    }

    public void setGiafigure(int giafigure) {
        this.giafigure = giafigure;
    }
}
